const mongoose = require('mongoose');
const { dataModels } = require('../utils/constants');

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    minlength: 2,
    maxlength: 30,
  },
  about: {
    type: String,
    required: true,
    minlength: 2,
    maxlength: 30,
  },
  avatar: {
    type: String,
    required: true,
  },
}, {
  toJSON: {
    versionKey: false,
  },
});

module.exports = mongoose.model(dataModels.user, userSchema);
